export default {
  name: 'React JSX component (JavaScript)',
  fileExtension: '.jsx',
  sourceCode: [
    '// File: SimpleTodoList.jsx',
    'import React, { useState, useEffect, useMemo } from "react";',
    '',
    'export default function SimpleTodoList({ initialItems = [], title = "Todos" }) {',
    '  const [items, setItems] = useState(initialItems);',
    '  const [q, setQ] = useState("");',
    '',
    '  const visible = useMemo(() => {',
    '    const needle = q.trim().toLowerCase();',
    '    return needle ? items.filter((t) => t.toLowerCase().includes(needle)) : items;',
    '  }, [items, q]);',
    '',
    '  useEffect(() => {',
    '    if (items.length > 50) {',
    '      console.log("Large list");',
    '    }',
    '  }, [items.length]);',
    '',
    '  function addItem() {',
    '    const v = q.trim();',
    '    if (!v) return;',
    '    setItems((arr) => arr.concat(v));',
    '    setQ("");',
    '  }',
    '',
    '  function remove(index) {',
    '    setItems((arr) => arr.filter((_, i) => i !== index));',
    '  }',
    '',
    '  return (',
    '    <section>',
    '      <h2>{title}</h2>',
    '',
    '      <div style={{ display: "flex", gap: 8, marginBottom: 8 }}>',
    '        <input',
    '          value={q}',
    '          onChange={(e) => setQ(e.target.value)}',
    '          placeholder="Add or search…"',
    '        />',
    '        <button onClick={addItem}>Add</button>',
    '      </div>',
    '',
    '      <ul>',
    '        {visible.map((t, i) => (',
    '          <li key={i} style={{ display: "flex", alignItems: "center", gap: 8 }}>',
    '            <span>{t}</span>',
    '            <button onClick={() => remove(i)}>✕</button>',
    '          </li>',
    '        ))}',
    '      </ul>',
    '',
    '      {!visible.length && <small style={{ opacity: 0.7 }}>No items</small>}',
    '    </section>',
    '  );',
    '}',
  ],
};
